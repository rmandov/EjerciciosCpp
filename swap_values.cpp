#include <iostream>
using namespace std;

int main()
{
    // program for swapping values of two variables
    
    int a = 20;
    int b = 10;
    
    a = a + b; // 30
    b = a - b; // 20
    a = a - b; // 10

    cout << "a = " << a << endl;
    cout << "b = " << b;
    
    system("pause>0");
}